<p>Concebida por Kathya Valdez Gómez y programada por Estela Saraí Cortés Córdova, esta página se creó como propuesta de una herramienta didáctica digital que favoreciera la pronunciación del francés de los estudiantes de canto de JazzUV.</p>
<p>Es didáctica porque fue concebida para usarse en una modalidad semipresencial, ya que al ser su objetivo favorecer la pronunciación del francés en los estudiantes de canto de jazzUV se necesitaría de tecnología de reconocimiento de voz y también de reconocimiento de frecuencias (para la afinación) y eso requeriría una mayor inversión económica, así como una planeación técnica que implicaría un equipo interdisciplinario más amplio para su logro.</p>
<h5 class="titles">Modo de uso</h5>
<hr>

<p>Nuestra herramienta didáctica digital se presenta en un formato de página web con una interfaz dividida en nueve pestañas.
Aunque esté contemplado usarla en una modalidad semipresencial, si la visitas de manera autónoma te sugerimos visitar sus pestañas en el orden que aparecen de izquierda a derecha.</p>

<p><span>Inicio:</span> aquí se presenta la herramienta y su modo de uso.</p>

<p><span>Aparato fonador:</span> en esta pestaña puedes acceder a información escrita y visual del aparato fonador.</p>


<p><span>AFI:</span> en este espacio puedes conocer el Alfabeto Fonético Internacional y los símbolos estándar que utiliza para la transcripción fonética del francés.</p>

<p><span>Rasgos articulatorios:</span> en esta sección puedes acceder a información escrita y visual sobre los rasgos articulatorios de los fonemas.</p>

<p><span>Fenómenos fonéticos:</span> en este apartado puedes encontrar información sobre ciertos fenómenos fonéticos del francés.</p>

<p><span>Comprensión oral:</span> aquí puedes ver un video que contiene imágenes alusivas a la letra de la canción, así como la grabación de la canción. Al mismo tiempo, puedes controlar la barra de desplazamiento del objeto de la derecha para mostrar la letra de la canción en francés y su traducción al español, para así comprender globalmente el significado de la canción.</p>

<p><span>Herramienta:</span> en esta pestaña puedes acceder a la letra de la canción codificada por colores en base a una clasificación de los tipos de sonidos.</p>
<p>Arriba de la tabla encontrarás 12 botones correspondientes a las 12 estrofas de la canción. Te recomendamos visitarlas todas para conocer a detalle la pronunciación de toda la canción.</p>
<p>En un primer momento visualizarás lo presentado a continuación que contiene en la mitad izquierda la letra de la canción en francés, su transcripción fonética, un audio  de la estrofa y su traducción al español, y en la mitad derecha un menú.</p>
