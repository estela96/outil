<p>Te recomendamos leer la transcripción fonética en voz alta, y si tienes dudas sobre la pronunciación de alguno de los símbolos podrás dar click sobre las letras de colores de la sección de la letra de la canción en francés o sobre el menú; ambos direccionan al mismo contenido. </p>
<p>Nota: aclaramos que las transcripciones fonéticas no suelen llevar espacios, pero hemos decidido incluirlos para hacer más evidente la correspondencia de las palabras de la letra de la canción con la transcripción fonética.</p>
<p>Después de dar click sobre las letras de colores o sobre el menú en la mitad derecha aparecerán todas las palabras, que contengan este tipo de sonidos, con sus respectivos audios. </p>
