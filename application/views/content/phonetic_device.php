<h3 class="subtitle">Partes:</h3>
<ul>

  <li><span>Tórax:</span> regula la salida del aire de los pulmones (hablamos durante la fase de expiración).  (Freland-Ricard, 2002) </li>
  <li><span>Pulmón:</span> es uno de los órganos de la respiración del hombre, de estructura esponjosa, blando, flexible, que se comprime y se dilata, y ocupa una parte de la cavidad torácica. (ASALE, 2014)</li>
  <li><span>Laringe:</span> es un órgano tubular, compuesto por varios cartílagos (en la mayoría de los vertebrados), que comunica por un lado con la faringe y por otro con la tráquea. (ASALE, 2014)</li>
  <li><span>Cuerdas vocales:</span> son músculos que parecen labios gruesos y pueden juntarse y separarse. La vibración o ausencia de vibración de las cuerdas determina si el sonido es sonoro o sordo. (Freland-Ricard, 2002)</li>
  <li><span>Labios:</span> sirven para modificar la forma de la boca (Freland-Ricard, 2002).</li>
  <li><span>Lengua:</span> su desplazamiento de adelante hacia atrás y de arriba hacia abajo permite modificar el volumen del canal bucal, lo que permite la producción de sonidos diferentes (vocales anteriores o posteriores, y abiertas o cerradas) (Freland-Ricard, 2002).</li>
  <li><span>Glotis:</span> es la abertura entre las cuerdas vocales. (Freland-Ricard, 2002)</li>
  <li><span>Faringe:</span> porción ensanchada del tubo digestivo situada a continuación de la boca, que en el hombre tiene varias aberturas por las que comunica con las fosas nasales, la trompa de Eustaquio, la laringe y el esófago. (ASALE, 2014)</li>
  <li><span>Úvula:</span> abre o cierra el acceso a las fosas nasales para obtener vocales nasales u orales. (Freland-Ricard, 2002)</li>
  <li><span>Velo del paladar:</span> Especie de cortina muscular y membranosa que separa la cavidad bucal de la faringe. (ASALE, 2014)</li>
  <li><span>Alvéolo dental:</span> cada una de las cavidades en que están engastados los dientes en las mandíbulas. (ASALE, 2014)</li>

</ul>
