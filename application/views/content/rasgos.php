<h5 class="titles">RASGOS ARTICULATORIOS</h5>
<hr>
<div class="p_span">

<p>La fonética articulatoria describe los <span>rasgos articulatorios de los fonemas</span>, tales como: </p>
<ul>
  <li><span>Lugar de articulación.-</span> Este rasgo se refiere al lugar (anterior o posterior) en la boca hacia el cual se aglomera la lengua. (Abry & Veldeman-Abry, 2007)</li>
  <li><span>Apertura.-</span> Este rasgo se refiere a la altura de la lengua en relación con el paladar y la abertura del maxilar. Se consideran cuatro grados: cerrado / alto, medio cerrado / medio alto, medio abierto / medio bajo y abierto / bajo. (Abry & Veldeman-Abry, 2007)</li>

  <li><span>Redondeo de los labios (retracción):</span> este rasgo se refiere al papel de los labios, que pueden o no redondearse y proyectarse hacia adelante. (Abry & Veldeman-Abry, 2007)</li>

  <li><span>Modo oclusivo / constrictivo.-</span> La oposición de este modo se refiere a si se efectúa o no una obstrucción o estrechamiento del aire por la articulación de los órganos del aparato fonador (Abry y Veldeman-Abry, 2007)</li>

  <li><span>Modo sordo / sonoro.-</span> Este modo se refiere a la vibración presente o ausente en las cuerdas vocales. (Abry & Veldeman-Abry, 2007)</li>

  <li><span>Modo oral / nasal.-</span> Este modo se refiere a si el aire pasa o no a través de la cavidad vocal y la cavidad nasal al mismo tiempo al bajar el paladar blando. (Abry & Veldeman-Abry, 2007)</li>

  <li><span>Modo medial / lateral.-</span> Este modo se refiere a si el aire pasa por un canal mediano de la lengua o a los lados de ésta. (Abry & Veldeman-Abry, 2007)</li>

</ul>

<p>Estos rasgos son considerados en la definición articulatoria de cada fonema presentado en la pestaña “Herramienta”.</p>

