<h5 class="titles">ALFABETO FONÉTICO INTERNACIONAL</h5>
<hr>
<div class="text_light">
  <p>La International Phonetic Association (también ocupa las siglas IPA) proporciona un estándar global para la representación fonética de todos los idiomas.(International Phonetic Association)</p>
  <p>Según la RAE (2014), transcribir significa copiar. La <span>transcripción fonética</span> se utiliza para trabajar la fonética desde una perspectiva visual. Los fonogramas ayudan a descifrar la correspondencia de los gráficos de ortografía francesa con sus sonidos correspondientes. La transcripción fonética utiliza la notación fonética y la ortografía fonética, entre otros elementos.</p>
  <p>La <span>notación fonética</span> es un sistema de representación de los sonidos del lenguaje donde un mismo signo gráfico corresponde siempre a un mismo sonido.  (Outil de consultation du Dictionnaire de l'Académie Française)</p>
  <p>La <span>ortografía fonética</span> consiste en transcribir las palabras tal cual son pronunciadas. (Outil de consultation du Dictionnaire de l'Académie Française)</p>
  <p>Hoy en día, el AFI se utiliza en la mayoría de los diccionarios para representar la pronunciación de las palabras que aparecen como entradas. La transcripción fonética de un sonido, palabra o frase siempre aparece entre corchetes. Para representar los sonidos del francés, se usan 36 símbolos fonéticos: 16 para las vocales, 17 para las consonantes y 3 para las semi-vocales (también llamadas semi-consonantes). (Office québécois de la langue française, 2002).</p>

</div>

